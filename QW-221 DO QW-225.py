def compute_casimir_energy(omega, phi, alpha_geo, beta_tors, L_values):
    """
    Oblicza energię Casimira dla płyt w odległości L.

    Metoda: Porównanie energii próżni z i bez warunków brzegowych.
    """
    casimir_energies = []

    for L in L_values:
        # Rozmiar systemu (liczba stanów między płytami)
        N = int(L)
        if N < 4:
            N = 4  # Minimum dla sensownych obliczeń

        # Macierz z warunkami brzegowymi (zerowanie na końcach)
        S_bounded = build_octonion_coupling_matrix(N, omega, phi, alpha_geo, beta_tors)

        # Wymuszenie warunków brzegowych: |ψ(0)⟩ = |ψ(L)⟩ = 0
        # Modyfikujemy macierz na końcach
        S_bounded[0, :] = 0
        S_bounded[:, 0] = 0
        S_bounded[-1, :] = 0
        S_bounded[:, -1] = 0
        S_bounded[0, 0] = 1e6  # Duża energia (stan niemożliwy)
        S_bounded[-1, -1] = 1e6

        # Macierz bez warunków brzegowych (próżnia swobodna)
        S_free = build_octonion_coupling_matrix(N, omega, phi, alpha_geo, beta_tors)

        # Energia punktu zerowego: E_0 = (1/2) Σ |λ_i|
        evals_bounded = linalg.eigvalsh(S_bounded)
        evals_free = linalg.eigvalsh(S_free)

        # Usuń sztuczne stany brzegowe
        evals_bounded_clean = evals_bounded[evals_bounded < 1e5]

        # Energia próżni
        E_bounded = 0.5 * np.sum(np.abs(evals_bounded_clean))
        E_free = 0.5 * np.sum(np.abs(evals_free))

