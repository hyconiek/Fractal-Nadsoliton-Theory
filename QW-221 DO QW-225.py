def analyze_mass_gap(omega, phi, alpha_geo, beta_tors, N_values):
    """
    Analizuje najniższą wartość własną dla różnych rozmiarów systemu.

    Celem jest sprawdzenie, czy λ_0 → 0 gdy N → ∞ (foton bezmasowy)
    czy λ_0 > 0 (masa fotonu w teorii Proca).
    """
    results = []

    for N in N_values:
        S = build_octonion_coupling_matrix(N, omega, phi, alpha_geo, beta_tors)
        evals = np.sort(linalg.eigvalsh(S))

        # Znajdź najniższą wartość własną (w wartości bezwzględnej)
        # która jest najbliższa zeru
        abs_evals = np.abs(evals)
        lambda_0 = abs_evals[np.argmin(abs_evals)]

        # Średnia odstępu między poziomami
        level_spacing = np.mean(np.diff(np.sort(abs_evals)))

        results.append({
            'N': N,
            'lambda_0': lambda_0,
            'lambda_0_over_spacing': lambda_0 / level_spacing if level_spacing > 0 else np.nan
        })

    return pd.DataFrame(results)

