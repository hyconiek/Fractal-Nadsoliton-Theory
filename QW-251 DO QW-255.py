def compute_euler_characteristic_from_graph(S, threshold_connection=0.1):
    """
    Oblicza charakterystykę Eulera χ = V - E + F dla grafu interakcji.

    Metoda: Graf reprezentuje strukturę oktawową.
    - Wierzchołki (V): stany (mody)
    - Krawędzie (E): sprzężenia (elementy pozadiagonalne)
    - Ściany (F): cykle w grafie
    """
    N = S.shape[0]

    # Liczba wierzchołków
    V = N

    # Liczba krawędzi: połączenia z |S_ij| > threshold
    adjacency = np.abs(S) > threshold_connection
    # Tylko górny trójkąt (nieskierowany graf)
    E = np.sum(np.triu(adjacency, k=1))

    # Liczba ścian: estymacja z cykli
    # Dla sieci 2D: F ≈ E - V + χ (formuła Eulera)
    # Dla torusa (χ=0): F = E - V
    # Dla sfery (χ=2): F = E - V + 2

    # Szukamy małych cykli (3-wierzchołkowe, 4-wierzchołkowe)
    # Liczba trójkątów (3-cycles)
    A = adjacency.astype(int)
    triangles = 0
    for i in range(N):
        for j in range(i+1, N):
            if A[i, j]:
                for k in range(j+1, N):
                    if A[i, k] and A[j, k]:
                        triangles += 1

    # Estymacja ścian: każdy trójkąt to potencjalna ściana
    # Plus kwadraty (4-cycles)
    squares = 0
    for i in range(N):
        for j in range(i+1, N):
            if A[i, j]:
                for k in range(j+1, N):
                    if A[i, k]:
                        for l in range(k+1, N):
                            if A[j, l] and A[k, l]:
                                squares += 1

    # Przybliżona liczba ścian (trójkąty + kwadraty)
    F = triangles + squares

    # Charakterystyka Eulera
    chi = V - E + F

    return chi, V, E, F, triangles, squares

