def compute_initial_singularity_entropy(N, concentration_param=0.1):
    """
    Symuluje stan osobliwości: wszystkie mody skompresowane do małej objętości.

    Parametry:
    - N: liczba modów
    - concentration_param: stopień koncentracji (mały = silna singularność)

    Zwraca entropię von Neumanna stanu.
    """
    # Stan początkowy: Gaussowska koncentracja wokół centrum
    positions = np.arange(N)
    center = N / 2

    # Amplitudy stanów - Gaussowska z małą szerokością
    sigma = concentration_param * N
    amplitudes = np.exp(-(positions - center)**2 / (2 * sigma**2))
    amplitudes = amplitudes / np.sqrt(np.sum(amplitudes**2))  # Normalizacja

    # Macierz gęstości: ρ = |ψ⟩⟨ψ|
    rho = np.outer(amplitudes, amplitudes.conj())

    # Diagonalizacja macierzy gęstości
    eigenvalues = linalg.eigvalsh(rho)
    eigenvalues = eigenvalues[eigenvalues > 1e-15]  # Usunięcie zer numerycznych

    # Entropia von Neumanna: S = -Tr(ρ log ρ) = -∑ λ_i log(λ_i)
    entropy = -np.sum(eigenvalues * np.log(eigenvalues))

    # Maksymalna entropia dla N stanów: S_max = log(N)
    S_max = np.log(N)

    # Względna entropia
    S_relative = entropy / S_max if S_max > 0 else 0

    return entropy, S_relative, eigenvalues

