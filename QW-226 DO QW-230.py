# QW-227: STATYSTYKA FERMIEGO-DIRACA Z TOPOLOGII
# ===============================================
# Cel: Sprawdzenie, dlaczego elektrony nie spadają na jądro (zakaz Pauliego)
# Hipoteza: Dwa identyczne fermiony w tym samym stanie odczuwają nieskończoną repulsję
#           z nieliniowego członu A⁴ w Lagrangianie

print("\n" + "=" * 80)
print("QW-227: STATYSTYKA FERMIEGO-DIRACA Z TOPOLOGII")
print("=" * 80)

def compute_interaction_energy_two_fermions(S, state_index, separation_param=0.1):
    """
    Oblicza energię oddziaływania dwóch identycznych fermionów
    umieszczonych w tym samym (lub bliskim) stanie kwantowym.

    Metoda: Nieliniowe sprzężenie A⁴ powoduje repulsję przy nakładaniu funkcji falowych.
    """
    N = S.shape[0]

    # Diagonalizacja do uzyskania stanów własnych
    evals, evecs = linalg.eigh(S)

    # Wybór stanu do testowania
    if state_index >= N:
        state_index = N // 2  # Stan w środku widma

    # Funkcja falowa wybranego stanu
    psi_state = evecs[:, state_index]

    # Obliczenie energii nieliniowego członu A⁴
    # Dla jednego fermionu: E₁ = ⟨ψ|H|ψ⟩
    E_single = np.real(np.dot(psi_state.conj(), S @ psi_state))

    # Dla dwóch fermionów w tym samym stanie (bosony): |ψ + ψ⟩ = √2|ψ⟩
    # Energia nieliniowa: E₂ = ⟨2ψ|H_nl|2ψ⟩ ~ 4⁴ ⟨ψ|H_nl|ψ⟩ dla A⁴

    # Uproszczony model nieliniowości: E_nl ~ |A|⁴
    # Gęstość prawdopodobieństwa
    rho_1 = np.abs(psi_state)**2
    rho_2_same = np.abs(psi_state + psi_state)**2  # Dwa w tym samym stanie

    # Energia nieliniowa proporcjonalna do ρ²
    E_nl_1 = np.sum(rho_1**2) * beta_tors  # Parametr torsji β jako coupling
    E_nl_2_same = np.sum(rho_2_same**2) * beta_tors

    # Energia oddziaływania (ponad liniową)
    E_interaction_same = E_nl_2_same - 2 * E_nl_1

    # Dla dwóch fermionów w różnych stanach (separacja)
    # Antysymetryczna kombinacja: |ψ₁⟩|ψ₂⟩ - |ψ₂⟩|ψ₁⟩
    if state_index + 1 < N:
        psi_state2 = evecs[:, state_index + 1]
    else:
        psi_state2 = evecs[:, state_index - 1]

    # Kombinacja antysymetryczna (approximacja)
    psi_antisym = (psi_state - psi_state2) / np.sqrt(2)
    rho_2_diff = np.abs(psi_antisym)**2

