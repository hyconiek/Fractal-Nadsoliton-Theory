# ================================================
# Cel: Test fizyki wysokich energii (QCD)
# Hipoteza: Gęstość stanów ρ(E) ~ exp(E/T_H) powyżej pewnej energii

print("\n" + "=" * 80)
print("QW-229: TEMPERATURA HAGEDORNA (GRANICA MATERII)")
print("=" * 80)

def analyze_density_of_states_hagedorn(S):
    """
    Analizuje gęstość stanów ρ(E) dla wysokich energii.
    Temperatura Hagedorna: Punkt, powyżej którego ρ(E) ~ exp(E/T_H).
    """
    # Wartości własne (energie)
    evals = np.sort(np.real(linalg.eigvalsh(S)))
    N = len(evals)

    # Histogram energii (gęstość stanów)
    n_bins = min(50, N // 4)
    counts, bin_edges = np.histogram(evals, bins=n_bins)
    bin_centers = (bin_edges[:-1] + bin_edges[1:]) / 2

    # Gęstość stanów ρ(E) = dN/dE
    bin_width = bin_edges[1] - bin_edges[0]
    rho_E = counts / bin_width

    # Usuń zera
    nonzero_mask = rho_E > 0
    E_nonzero = bin_centers[nonzero_mask]
    rho_nonzero = rho_E[nonzero_mask]

    if len(E_nonzero) < 5:
        return None, None, None, None, None

